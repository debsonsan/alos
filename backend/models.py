	applicant_prior_positive_lender_relationship = Column(Boolean, nullable=False, default=False)

	has_co_applicant = Column(Boolean, nullable=False, default=False)
	co_applicant_id = Column(String(64), nullable=True, index=True)
	co_applicant_legal_name = Column(String(255), nullable=True)
	co_applicant_date_of_birth = Column(Date, nullable=True)
	co_applicant_tax_id_token = Column(String(255), nullable=True)
	co_applicant_address = Column(Text, nullable=True)
	co_applicant_state = Column(String(32), nullable=True)
	co_applicant_residence_duration_months = Column(Integer, nullable=True)
	co_applicant_employment_status = Column(String(64), nullable=True)
	co_applicant_employer_name = Column(String(255), nullable=True)
	co_applicant_employment_duration_months = Column(Integer, nullable=True)
