	co_applicant_monthly_housing_payment = Column(Numeric(12, 2), nullable=True)
	co_applicant_credit_score = Column(Integer, nullable=True)
	co_applicant_credit_history_depth_months = Column(Integer, nullable=True)
	co_applicant_recent_delinquencies = Column(Integer, nullable=True)
	co_applicant_recent_severe_delinquencies = Column(Integer, nullable=True)
	co_applicant_bankruptcies = Column(Integer, nullable=True)
	co_applicant_open_bankruptcy = Column(Boolean, nullable=True)
	co_applicant_recent_repossessions = Column(Integer, nullable=True)
	co_applicant_charge_offs = Column(Integer, nullable=True)
	co_applicant_recent_inquiries = Column(Integer, nullable=True)
