	co_applicant_tax_id_valid = Column(Boolean, nullable=True)
	co_applicant_deceased_tax_id_flag = Column(Boolean, nullable=True)

	vin = Column(String(32), nullable=False, index=True)
	vehicle_model_year = Column(Integer, nullable=False)
	vehicle_make = Column(String(128), nullable=False)
	vehicle_model = Column(String(128), nullable=False)
